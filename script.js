        {pergunta:"Qual é a região onde se encontra a maior reserva de diamantes de Angola?", opcoes:["Lunda Norte","Lunda Sul","Moxico","Cuando Cubango"], correta:0},
        {pergunta:"Qual foi o primeiro tratado internacional assinado por Angola após a independência?", opcoes:["Tratado com Portugal","Tratado com Cuba","Tratado com a União Soviética","Tratado com os EUA"], correta:0}
