    function startGame() {
        currentNivel = "facil";
        currentIndex = 0;
        levelScore = 0;
        lives = 5;
        finalTotalScore = 0;
        helpUsed = false;
        stats = { correct: 0, wrong: 0 };
        pendingTransition = false;
        loadLevel();
    }
    
    function loadLevel() {
        let perguntas = dbMap[selectedArea][currentNivel];
        if(!perguntas || perguntas.length === 0) {
            finishGameWin();
            return;
        }
        currentQuestionsList = shuffleArray([...perguntas]);
        totalQuestionsInLevel = currentQuestionsList.length;
        currentIndex = 0;
        levelScore = 0;
        answered = false;
        currentScreen = "quiz";
        render();
    }
    
    function finishGameWin() {
        localStorage.setItem("lastScore", finalTotalScore);
        currentScreen = "result";
        render();
    }
    
    function gameOver() {
        localStorage.setItem("lastScore", finalTotalScore);
        showModal("💀 GAME OVER", `Você perdeu todas as vidas!\nPontuação final: ${finalTotalScore} pontos.`, () => {
            currentScreen = "welcome";
            render();
        }, true);
    }
    
    function advanceToNextLevel() {
        const ordem = ["facil","medio","dificil","especial"];
        const idx = ordem.indexOf(currentNivel);
        if(idx + 1 < ordem.length) {
            currentNivel = ordem[idx+1];
            loadLevel();
        } else {
            finishGameWin();
        }
    }
    
    function handleHelp() {
        if(helpUsed) {
            showModal("⚠️ Ajuda", "Você já usou sua única ajuda nesta partida!", null, true);
            return;
        }
        if(!currentQuestionData) return;
        helpUsed = true;
        const correctText = currentQuestionData.opcoes[currentQuestionData.correta];
        showModal("💡 DICA", `A resposta correta é: "${correctText}"`, null, true);
    }
    
    function showStatistics() {
        const total = stats.correct + stats.wrong;
        const statsHtml = `
            <div class="stat-item"><strong>✅ Acertos:</strong> <span>${stats.correct}</span></div>
            <div class="stat-item"><strong>❌ Erros:</strong> <span>${stats.wrong}</span></div>
            <div class="stat-item"><strong>❤️ Vidas restantes:</strong> <span>${lives}</span></div>
            <div class="stat-item"><strong>🏆 Pontuação total:</strong> <span>${finalTotalScore}</span></div>
            <div class="stat-item"><strong>📊 Taxa de acerto:</strong> <span>${total > 0 ? Math.round((stats.correct/total)*100) : 0}%</span></div>
        `;
        showModal("📊 Estatísticas da Partida", statsHtml, null, true);
    }
    
    function handleAnswer(selectedIdx, correctIdx, questionObj) {
        if(answered || pendingTransition) return;
        answered = true;
        const isCorrect = (selectedIdx === correctIdx);
        
        if(isCorrect) {
            levelScore++;
            finalTotalScore++;
            stats.correct++;
            showModal("✅ Correto!", "+1 ponto! Continue assim.", null, true);
        } else {
            lives--;
            stats.wrong++;
            showModal("❌ Errado!", `A resposta correta era: ${questionObj.opcoes[correctIdx]}. Você perdeu 1 vida. Restam ${lives} vidas.`, null, true);
        }
        
        setTimeout(() => {
            if(lives <= 0) {
                gameOver();
                return;
            }
            
            if(currentIndex + 1 < totalQuestionsInLevel) {
                currentIndex++;
                answered = false;
                render();
            } else {
                pendingTransition = true;
                showModal("🎉 Nível Concluído!", `Você completou o nível ${currentNivel.toUpperCase()}!\nAcertos: ${levelScore}/${totalQuestionsInLevel}\nVidas restantes: ${lives}\nAvançando para o próximo nível...`, () => {
                    pendingTransition = false;
                    advanceToNextLevel();
                }, true);
            }
        }, 1800);
    }
    
    function goToMainMenu() {
        if(pendingTransition) return;
        showModal("⚠️ Sair do Jogo", "Tem certeza que deseja voltar à tela inicial? Seu progresso será perdido.", () => {
            currentScreen = "welcome";
            render();
        }, true);
    }
    
    function renderQuiz() {
        if(currentIndex >= currentQuestionsList.length) return;
        const originalQ = currentQuestionsList[currentIndex];
        currentQuestionData = randomizeQuestionOptions(originalQ);
        const q = currentQuestionData;
        const nivelNome = {facil:"FÁCIL", medio:"MÉDIO", dificil:"DIFÍCIL", especial:"ESPECIAL"}[currentNivel];
        const progresso = `${currentIndex+1}/${totalQuestionsInLevel} • Acertos nível: ${levelScore}`;
        const hearts = "❤️".repeat(lives) + "🖤".repeat(5-lives);
        
        const opcoesHtml = q.opcoes.map((opt,idx)=> `<button class="opcao-btn" data-idx="${idx}">${String.fromCharCode(65+idx)}. ${opt}</button>`).join("");
        
        root.innerHTML = `
            <div class="card">
                <div class="quiz-header">
                    <span class="area-badge">${selectedArea.toUpperCase()}</span>
                    <span class="nivel-badge">${nivelNome}</span>
                    <span class="lives-badge">${hearts}</span>
                    <span class="progresso-texto">${progresso}</span>
                </div>
                <div class="pergunta-texto">${q.pergunta}</div>
                <div class="opcoes-lista" id="opcoesLista">
                    ${opcoesHtml}
                </div>
                <div class="flex-buttons">
                    <button class="btn btn-secondary" id="statsBtn">📊 Estatísticas</button>
                    <button class="btn btn-danger" id="voltarMenuBtn">🏠 Voltar</button>
                    <button class="btn btn-help" id="helpBtn">💡 Ajuda (1 vez)</button>
                </div>
                <div class="small-note">Acertando mais da metade avança de nível. Cada erro = -1 vida</div>
            </div>
        `;
        
        document.querySelectorAll(".opcao-btn").forEach(btn => {
            btn.addEventListener("click", (e) => {
                if(answered || pendingTransition) return;
                const idx = parseInt(btn.dataset.idx);
                handleAnswer(idx, q.correta, q);
            });
        });
        
        document.getElementById("statsBtn")?.addEventListener("click", () => showStatistics());
        document.getElementById("helpBtn")?.addEventListener("click", () => handleHelp());
        document.getElementById("voltarMenuBtn")?.addEventListener("click", () => goToMainMenu());
    }
    
